// All 15 languages populated by Sprint 12's i18n-translator pass. Bundles
// pending native-speaker review before each affiliate handover, but the UI
// compiles + runs against every language today.

const DICTIONARIES: Record<AdminLangCode, AdminTranslations> = {
  en: EN,
  es: ES, de: DE, fr: FR, it: IT, pt: PT,
  nl: NL, pl: PL, hr: HR, sv: SV,
  ar: AR, ja: JA, th: TH, id: ID, tr: TR,
}

export function tAdmin(key: AdminTranslationKeys, lang: AdminLangCode, vars?: Record<string, string | number>): string {
  const raw = DICTIONARIES[lang]?.[key] ?? EN[key] ?? key
  if (!vars) return raw
  return Object.keys(vars).reduce((s, k) => s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(vars[k])), raw)
}

// --- Language context ---
// Every component reads the active admin language from this context. The
// Dashboard mounts the provider near the top of the tree so the whole panel
// rerenders when the language flips.
interface AdminLangContextValue {
  lang: AdminLangCode
  setLang: (c: AdminLangCode) => void
}

export const AdminLangContext = createContext<AdminLangContextValue>({
  lang: 'en',
  setLang: () => { /* overridden by provider */ },
})

export function useAdminLang(): AdminLangContextValue {
  return useContext(AdminLangContext)
}

// Short hook for the common "get the current lang + a bound t()" pattern.
export function useT(): {
  lang: AdminLangCode
  t: (key: AdminTranslationKeys, vars?: Record<string, string | number>) => string
} {
  const { lang } = useAdminLang()
  return {
    lang,
    t: (key, vars) => tAdmin(key, lang, vars),
  }
}

// --- Persistence helpers ---
// localStorage key + navigator fallback so a fresh admin reload returns to
// the operator's last choice (and, on first visit, to the browser's preferred
// language when it's one we support).
const STORAGE_KEY = 'cbc_admin_lang'
const SUPPORTED_CODES = new Set<AdminLangCode>(ADMIN_LANGUAGES.map(l => l.code))

export function loadStoredAdminLang(): AdminLangCode {
  if (typeof window === 'undefined') return 'en'
  try {
    const stored = window.localStorage.getItem(STORAGE_KEY) as AdminLangCode | null
    if (stored && SUPPORTED_CODES.has(stored)) return stored
  } catch { /* ignore */ }
  const nav = (window.navigator.language || 'en').slice(0, 2).toLowerCase() as AdminLangCode
  return SUPPORTED_CODES.has(nav) ? nav : 'en'
}

export function persistAdminLang(code: AdminLangCode): void {
  try { window.localStorage.setItem(STORAGE_KEY, code) } catch { /* ignore */ }
}
